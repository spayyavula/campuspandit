import React, { useState } from 'react';
import { supabase } from '../utils/supabase';
import { Mail, AlertTriangle, CheckCircle, RefreshCw, Settings } from 'lucide-react';

const EmailTroubleshooter: React.FC = () => {
  const [email, setEmail] = useState('');
  const [results, setResults] = useState<string[]>([]);
  const [loading, setLoading] = useState(false);

  const addResult = (message: string) => {
    setResults(prev => [...prev, message]);
  };

  const clearResults = () => {
    setResults([]);
  };

  const testEmailConfiguration = async () => {
    if (!email) {
      addResult('‚ùå Please enter an email address first');
      return;
    }

    setLoading(true);
    clearResults();
    
    addResult('üîç Starting email configuration test...');
    
    try {
      // Test 1: Try registration to see email behavior
      addResult('\nüìù Testing registration process...');
      const testEmail = email.includes('+test') ? email : email.replace('@', '+test@');
      
      const { data, error } = await supabase.auth.signUp({
        email: testEmail,
        password: 'test123456',
        options: {
          data: { name: 'Test User' }
        }
      });
      
      if (error) {
        if (error.message.includes('already registered')) {
          addResult('‚ÑπÔ∏è Email already registered - this is normal for testing');
          // Try resend instead
          await testResendEmail(email);
        } else {
          addResult(`‚ùå Registration failed: ${error.message}`);
        }
        return;
      }
      
      addResult('‚úÖ Registration request completed');
      
      // Analyze results
      if (data.session) {
        addResult('üü¢ EMAIL CONFIRMATION IS DISABLED');
        addResult('   ‚Üí Users are automatically logged in');
        addResult('   ‚Üí No verification emails are sent');
        addResult('   ‚Üí This is why you\'re not receiving emails');
      } else if (data.user?.confirmation_sent_at) {
        addResult('üü° EMAIL CONFIRMATION IS ENABLED');
        addResult('   ‚Üí Verification email should have been sent');
        addResult('   ‚Üí Check your email (including spam folder)');
        addResult(`   ‚Üí Email sent at: ${data.user.confirmation_sent_at}`);
      } else {
        addResult('üî¥ EMAIL CONFIGURATION PROBLEM');
        addResult('   ‚Üí Email confirmation is enabled but email was not sent');
        addResult('   ‚Üí Check Supabase SMTP configuration');
      }
      
    } catch (error) {
      addResult(`‚ùå Test failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
    } finally {
      setLoading(false);
    }
  };

  const testResendEmail = async (emailToTest?: string) => {
    const targetEmail = emailToTest || email;
    if (!targetEmail) return;
    
    addResult('\nüîÑ Testing email resend...');
    
    try {
      const { error } = await supabase.auth.resend({
        type: 'signup',
        email: targetEmail
      });
      
      if (error) {
        if (error.message.includes('rate limit')) {
          addResult('‚è±Ô∏è Rate limited - wait 60 seconds before trying again');
        } else if (error.message.includes('not found')) {
          addResult('‚ùå User not found - email may not be registered');
        } else {
          addResult(`‚ùå Resend failed: ${error.message}`);
        }
      } else {
        addResult('‚úÖ Resend request completed');
        addResult('üìß If email is configured, you should receive it soon');
      }
    } catch (error) {
      addResult(`‚ùå Resend test failed: ${error instanceof Error ? error.message : 'Unknown error'}`);
    }
  };

  const showSolutions = () => {
    clearResults();
    addResult('üõ†Ô∏è SOLUTIONS FOR EMAIL ISSUES:\n');
    
    addResult('1. üìß IF NO EMAILS ARE BEING SENT:');
    addResult('   ‚Ä¢ Check Supabase Dashboard > Authentication > Settings');
    addResult('   ‚Ä¢ Verify "Enable email confirmations" is checked');
    addResult('   ‚Ä¢ Configure SMTP settings or use Supabase email service');
    addResult('   ‚Ä¢ Set up email templates\n');
    
    addResult('2. ‚ö° QUICK FIX FOR DEVELOPMENT:');
    addResult('   ‚Ä¢ Disable email confirmation in Supabase settings');
    addResult('   ‚Ä¢ Users will be auto-logged in after registration');
    addResult('   ‚Ä¢ Re-enable for production\n');
    
    addResult('3. üîß SMTP CONFIGURATION:');
    addResult('   ‚Ä¢ Use Gmail, SendGrid, or other SMTP provider');
    addResult('   ‚Ä¢ Verify host, port, username, password');
    addResult('   ‚Ä¢ Test with a simple email first\n');
    
    addResult('4. üåê URL CONFIGURATION:');
    addResult('   ‚Ä¢ Set correct Site URL in Supabase');
    addResult('   ‚Ä¢ Add redirect URLs for auth callbacks');
    addResult('   ‚Ä¢ Ensure URLs match your domain\n');
    
    addResult('5. üìã RATE LIMITS:');
    addResult('   ‚Ä¢ Supabase has email rate limits');
    addResult('   ‚Ä¢ Wait between resend attempts');
    addResult('   ‚Ä¢ Consider upgrading plan for higher limits');
  };

  return (
    <div className="bg-white bg-opacity-10 backdrop-blur-lg rounded-xl p-6 max-w-2xl mx-auto mt-8">
      <h3 className="text-xl font-bold text-white mb-4 flex items-center">
        <Mail className="w-5 h-5 mr-2" />
        Email Troubleshooter
      </h3>
      
      <div className="space-y-4">
        <div>
          <label className="block text-white text-sm mb-2">Email to test:</label>
          <input
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            className="w-full px-3 py-2 bg-white bg-opacity-20 text-white placeholder-white placeholder-opacity-70 rounded-lg border border-white border-opacity-30 focus:border-opacity-60 focus:outline-none"
            placeholder="Enter your email address"
          />
        </div>
        
        <div className="flex flex-wrap gap-2">
          <button
            onClick={testEmailConfiguration}
            disabled={loading || !email}
            className="flex items-center px-4 py-2 bg-blue-600 text-white rounded-lg hover:bg-blue-700 disabled:opacity-50 transition-colors text-sm"
          >
            <RefreshCw className={`w-4 h-4 mr-2 ${loading ? 'animate-spin' : ''}`} />
            {loading ? 'Testing...' : 'Test Email Config'}
          </button>
          
          <button
            onClick={() => testResendEmail()}
            disabled={loading || !email}
            className="flex items-center px-4 py-2 bg-purple-600 text-white rounded-lg hover:bg-purple-700 disabled:opacity-50 transition-colors text-sm"
          >
            <Mail className="w-4 h-4 mr-2" />
            Test Resend
          </button>
          
          <button
            onClick={showSolutions}
            className="flex items-center px-4 py-2 bg-green-600 text-white rounded-lg hover:bg-green-700 transition-colors text-sm"
          >
            <Settings className="w-4 h-4 mr-2" />
            Show Solutions
          </button>
          
          <button
            onClick={clearResults}
            className="px-4 py-2 bg-gray-600 text-white rounded-lg hover:bg-gray-700 transition-colors text-sm"
          >
            Clear
          </button>
        </div>
        
        {results.length > 0 && (
          <div className="bg-black bg-opacity-30 rounded-lg p-4 max-h-96 overflow-y-auto">
            <h4 className="text-white font-semibold mb-2 flex items-center">
              <CheckCircle className="w-4 h-4 mr-2" />
              Test Results:
            </h4>
            <div className="text-white text-sm font-mono whitespace-pre-wrap">
              {results.join('\n')}
            </div>
          </div>
        )}
        
        <div className="bg-yellow-500 bg-opacity-20 rounded-lg p-4">
          <div className="flex items-start">
            <AlertTriangle className="w-5 h-5 text-yellow-300 mr-2 flex-shrink-0 mt-0.5" />
            <div className="text-yellow-100 text-sm">
              <p className="font-semibold mb-1">Common Email Issues:</p>
              <ul className="space-y-1 text-xs">
                <li>‚Ä¢ Email confirmation disabled in Supabase ‚Üí No emails sent</li>
                <li>‚Ä¢ SMTP not configured ‚Üí Emails fail to send</li>
                <li>‚Ä¢ Rate limits ‚Üí Too many attempts too quickly</li>
                <li>‚Ä¢ Spam filters ‚Üí Emails go to spam folder</li>
                <li>‚Ä¢ Wrong redirect URLs ‚Üí Email links don't work</li>
              </ul>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default EmailTroubleshooter;
